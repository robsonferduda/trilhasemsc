                                        <p>
                                            Para quem gosta de uma caminhada fácil, mas não abre mão de uma bela paisagem, essa é a trilha ideal. Com acesso fácil, localizado no bairro do Campeche, além de proporcionar uma caminhada agradável e uma bela paisagem, ainda fica próximo de algumas das praias mais belas da região, sendo um convite para um banho de mar depois da trilha. Do alto dela é possível ter uma visão 360º da parte leste e sul da Ilha de Florianópolis.

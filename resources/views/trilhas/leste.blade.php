                                        <p>
                                            Para quem conhece a região leste da Ilha de Florianópolis, sabe da beleza das praias dessa região. E uma das formas de contemplar toda essa beleza é fazendo alguma das trilhas que ficam na região, com seus mirantes e vistas de tirar o fôlego. Vamos apresentar a bela trilha do Dolmén da Oração, que fica na parte alta da Praia da Galheta e tem uma visão linda da região.

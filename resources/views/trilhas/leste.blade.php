                                <div id="descricao">

                                    <p>
                                        A cidade de Florianópolis, como todos sabem, é a capital do estado de Santa Catarina. Uma cidade ainda com ares de cidade pequena, onde com 20 minutos de carro, saímos do centro da cidade e chegamos até o bucólico Sertão do Ribeirão, por exemplo.
                                    </p>
                                    <p>
                                        Mas talvez nem todos saibam, que essa cidade que divide seu território entre a porção ilhéu e continental, é dividida em 5 regiões: centro, leste, norte, sul e continental. 
                                    </p>
                                    <p>
                                        Se você está procurando trilhas em Florianópolis, conhecer essa divisão é muito pertinente e interessante, pois te ajuda a planejar de maneira mais eficiente seu passeio. 
                                    </p>
                                    <p>
                                        Se você já conhece essa divisão, certamente é em razão da escolha das praias, pois fica mais difícil conciliar no mesmo dia ou passeio praias em regiões muito distintas, como Joaquina (Leste) e Jurerê Internacional (Norte).
                                    </p>
                                    <p>
                                        Entre as diversas trilhas que fizemos na cidade, a divisão em regiões também é muito útil, sendo possível fazer circuitos e travessias entres as trilhas e praias com localização próxima. Então fomos buscar em nossos arquivos todas as trilhas que já fizemos nessa região e montamos esse pequeno roteiro resumido para vocês, intitulado carinhosamente de: Trilhas da Região Leste da Ilha de Florianópolis.
                                    </p>
                                    <p>
                                        As trilhas feitas nessa região tem uma característica muito interessante e convidativa, todas iniciam, finalizam e passam por alguma praia. E falando nelas, as praias, primeiro vamos listar quais fazem parte da região leste da ilha. São elas: Praia da Joaquina, Praia do Gravatá, Praia Mole, Praia da Galheta, Barra da Lagoa, Prainha da Barra da Lagoa e Praia do Moçambique. E claro, não poderia faltar, a nossa querida Lagoa da Conceição, que possui uma conexão com o mar pelo canal da Barra da Lagoa.
                                    </p>
                                    <p>
                                        Bem, quanto às trilhas, ao longo de muitas expedições e aventuras, pudemos mapear e conhecer diferentes caminhos que percorrem os morros e praias desta região. Partindo do Moçambique, uma praia quase intocada e chegando na Joaquina, foram vários quilômetros de belezas e paisagens de tirar o fôlego. Mas agora, sem mais delongas, vamos conhecer cada uma delas.
                                    </p>
                                    <br/>

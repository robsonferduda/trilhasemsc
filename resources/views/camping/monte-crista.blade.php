                                <p>
                                    Saber a localização do último ponto de hidratação é importante, pois isso pode ajudar a escolher onde irá acampar. Digo isso, pois quem escolhe por acampar no Monte Crista, tem algumas opções um pouco resumidas. Chagando próximo ao cume, a primeira clareira logo após o último ponto de hidratação, que fica perto do Guardião, abriga pelo menos umas 8 barracas e fica bem próximo da água. Ele também é protegido do vento, mas tem o ponto negativo de exigir que para ver o nascer e pôr do sol, tenha que fazer a parte final da trilha até o cume. Dali, cerca de uns 10 minutos, mas de subida intensa. 
                                </p>                                
                                <p>
                                    Se optar por ficar no topo, justamente para poder sair da barraca e já dar de cara com o cume e também com a visão panorâmica da região e do nascer e pôr do sol, suba por mais 10 minutos para atingir o cume. Mas nesse ponto, saiba que os locais são menos protegidos do vento e o espaço é pequeno.
                                </p>

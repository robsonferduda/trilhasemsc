                                <p><strong>Trilhas</strong></p>
                                <p>
                                    Obviamente que em um lugar com esse, não poderiam faltar opções de trilha. Como dito, as duas cachoeiras citadas acima precisam de trilha para chegar até elas, 
                                    mas com certeza a trilha mais procurada é a Trilha do Mirante, que sobe o paredão de pedras e dá uma visão panorâmica incrível de toda a Serra Furada.
                                </p>    
                                <p>
                                    Nesta oportunidade ficamos devendo um relato sobre ela, mas certamente o relato estará aqui em breve.

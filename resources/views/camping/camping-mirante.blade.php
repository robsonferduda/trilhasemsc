                                </div>
                                <p><strong>Cachoeiras</strong></p>
                                <p>
                                    Além das belas paisagens da montanha, outra opção para quem visita o camping é a visitação de algumas cachoeiras próximas do local. Existem duas principais que são 
                                    a Cachoeira do Puma, logo na entrada do camping e a Cachoeira do Funil. 
                                </p>   
                                <p>
                                    Quando fomos, conseguimos visitar a Cahoeira do Puma, que fica localizada ao final de uma pequena trilha de 400 metros logo na entrada do camping.
                                    Claro, se você subiu até a base da fazenda, essa distância será um pouco maior. Detalhamos esse caminho no relato que pode ser acessado <a href="https://trilhasemsc.com.br/grao-para/trilhas/leve/trilha-da-cachoeira-do-puma">aqui</a>.
                                </p>

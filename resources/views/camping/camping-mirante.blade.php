                                <p><strong>Estrutura</strong></p>
                                <p>
                                    Quanto a estrutura, logo na chegada nos deparamos com a Recepção do camping. Na verdade ela é a casa da Cris (responsável pelo camping) e de seus pais. O espaço é utilizado para guardar pertences dos campistas e 
                                    também possui geladeiras com bebibas e o famoso pastel frito na hora, que todo mundo chega e pede. Pastel grande, gostoso e com preço justo.
                                </p>   
                                <p>
                                    Logo abaixo da casa, existe um abrigo de madeira, que possui os banheiros e a cozinha coletiva. São dois banheiros ali, cada um com sanitários e chuveiro. 
                                    A cozinha disponível no local é bem equipada, com geladeira (onde vocẽ pode deixar suas comidas e bebidas), fogão a gás, fogão a lenha e alguns utensílios de cozinha.
                                </p>
                                <p>
                                    Parte importante da estrutura são as pequenas mesas de madeira dispostas pelo terreno do camping, que servem para sentar, curtir a paisagem e também um bom café da manhã. 
                                    Algumas delas são colocadas próximas ao balanço, que é outro ponto turístico do camping.
                                </p>
                                <div class="box_img">
                                    <h5>Balanço do Camping</h5>
                                    <img src="{{ url('public/img/camping/camping-mirante/balanco.jpg') }}" alt="Balanço no Camping Mirante">
                                    <legend class="border-none"><strong>Foto</strong>: Robson Fernando Duda</legend>
                                </div>
                                <p>
                                    Claro que falando da estrutura não podemos esquecer do lago. Na verdade, ele poderia ser o primeiro item dessa nossa lista, devido à sua importância na composição da paisagem. 
                                    Não bastasse o contraste entre montanhas e o belo céu azul, o lago ajuda a formar uma cena que mais parece uma pintura.
                                </p>
                                <p>
                                    Além de ser parte importante na composição da paisagem, ele também possui um pequeno deck de madeira, acessível via passarela (eue o próprio pais da Cris construiu), onde se pode curir 
                                    a paisagem e um belo café da tarde.
                                </p>
                                <div class="box_img">
                                    <h5>Vista do Lago</h5>

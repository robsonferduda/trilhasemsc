                                    <legend class="border-none"><strong>Foto</strong>: Robson Fernando Duda</legend>
                                </div>
                                <p>
                                    Outro atrativo do lago é a possíbilidade de fazer um pequeno passeio de barco e treinar os braços em uma remada pelas suas águas.
                                </p>
                                <div class="box_img">
                                    <h5>Passeio de Barco</h5>
